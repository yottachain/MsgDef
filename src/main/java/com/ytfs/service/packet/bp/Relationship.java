package com.ytfs.service.packet.bp;

public class Relationship {

    private String username;
    private String mpoolOwner;

    /**
     * @return the username
     */
    public String getUsername() {
        return username;
    }

    /**
     * @param username the username to set
     */
    public void setUsername(String username) {
        this.username = username;
    }

    /**
     * @return the mpoolOwner
     */
    public String getMpoolOwner() {
        return mpoolOwner;
    }

    /**
     * @param mpoolOwner the mpoolOwner to set
     */
    public void setMpoolOwner(String mpoolOwner) {
        this.mpoolOwner = mpoolOwner;
    }

}
